const TELEGRAM_USER_HASH = "trqmftdJXwRgh5Xu";

// Функция для конвертации timestamp в формат SQL datetime
function timestampToSQLDateTime(timestamp) {
  const date = new Date(timestamp);
  const year = date.getFullYear();
  const month = String(date.getMonth() + 1).padStart(2, "0");
  const day = String(date.getDate()).padStart(2, "0");
  const hours = String(date.getHours()).padStart(2, "0");
  const minutes = String(date.getMinutes()).padStart(2, "0");
  const seconds = String(date.getSeconds()).padStart(2, "0");

  return `${year}-${month}-${day} ${hours}:${minutes}:${seconds}`;
}

// Функция для экранирования кавычек в комментариях
function escapeComment(comment) {
  if (!comment) return null;
  // Заменяем одинарные кавычки на две одинарные (экранирование для SQL)
  return comment.replace(/'/g, "''");
}

// Генерируем SQL запросы
let id = 1;
const allInserts = [];

// Проходим по всем данным
for (const userId in inputData) {
  const userWeeks = inputData[userId];

  for (const week in userWeeks) {
    const entries = userWeeks[week];

    for (const entry of entries) {
      const score = entry.score;
      const comment = escapeComment(entry.comment || null);
      const createdAt = timestampToSQLDateTime(entry.created);

      // Формируем строку для вставки
      let insertRow;
      if (comment) {
        insertRow = `(${id}, ${score}, '${comment}', '${TELEGRAM_USER_HASH}', '${createdAt}')`;
      } else {
        insertRow = `(${id}, ${score}, NULL, '${TELEGRAM_USER_HASH}', '${createdAt}')`;
      }

      allInserts.push(insertRow);
      id++;
    }
  }
}

